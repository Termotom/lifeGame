Setup = function(xPos,yPos,matLife, canvas)
{
	var ctx = canvas.getContext('2d');
	var mat = new Array();
	mat.length = 100;
	for (var i=0; i<mat.length; i++) 
	{
		mat[i] = new Array();
		mat[i].length = 100;
		for (var j=0; j<mat[i].length; j++) 
		{
			mat[i][j] = 0;
		}
	}
	
	ctx.fillStyle = 'black';
	for (var i=0; i<matLife.length; i++) 
	{
		for (var j=0; j<matLife[i].length; j++) 
		{
			if(matLife[i][j] == 1)
			{
				mat[j+xPos][i+yPos] = matLife[i][j];
				ctx.fillRect((j+xPos)*4, (i+yPos)*4, 3,3);
			}

		}
	}
	return mat;
}


